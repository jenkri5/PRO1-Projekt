package ex3;

import java.time.LocalDateTime;

public class FlightTest {

    public static void main(String[] args) {
        Flight flight = new Flight("AY025", "Seoul");
        flight.setDeparture(LocalDateTime.of(2015, 10, 20, 17, 30));
        flight.setArrival(LocalDateTime.of(2015, 10, 21, 3, 0));

        flight.addPassenger("Arnold", 31);
        flight.addPassenger("Bruce", 59);
        flight.addPassenger("Dolph", 24);
        flight.addPassenger("Linda", 59);
        flight.addPassenger("Jennifer", 65);

        System.out
            .println("Flight " + flight.getFlightNumber() + " for " + flight.getDestination());
        System.out.println("============================");
        System.out.println("Departure time: " + flight.getDeparture());
        System.out.println("Arrival time: " + flight.getArrival());
        System.out.printf("Flight duration is planned to: %.1f hours%n", flight.durationInHours());
        System.out.println();
        if (flight.midnightFlight()) {
            System.out.println("This is a midnight flight.");
        }
        else {
            System.out.println("This is not a midnight fligt.");
        }

        System.out.println("Passengers: " + flight.getPassengers());
        System.out.println();

        boolean removed = flight.removePassenger("Bruce");
        if (removed) {
            System.out.println("Bruce removed from passenger list");
            System.out.println("Passengers: " + flight.getPassengers());
            System.out.println();
        }

        System.out.printf("Average age of passengers is %.1f", flight.averagePassengerAge());
    }

}
